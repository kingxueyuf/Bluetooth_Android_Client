package com.bean;

public class student {
	String StuNo;
	String StuName;

	public void setStuNo(String No) {
		this.StuNo = No;
	}

	public void setStuName(String Name) {
		this.StuName = Name;
	}

	public String getStuNo() {
		return this.StuNo;
	}

	public String getStuName() {
		return this.StuName;
	}

}